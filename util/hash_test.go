package util

import (
	"testing"
)

func TestHash(t *testing.T) {
  data1 := []byte{0x62}
  data2 := []byte{0xc3, 0x97}
  data3 := []byte{0xe2, 0x99, 0xa5}
  data4 := []byte{0xe1, 0x80, 0xb9, 0x32}
  data5 := []byte{
    0x01, 0xc0, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x14, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x04, 0x00,
    0x00, 0x00, 0x00, 0x14,
    0x00, 0x00, 0x00, 0x18,
    0x28, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x02, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
  }

  if Hash(nil, 0xbc9f1d34) != 0xbc9f1d34 {
    t.Fatalf("Hash error")
  }
  if Hash(data1, 0xbc9f1d34) != 0xef1345c4 {
    t.Fatalf("Hash error")
  }
  if Hash(data2, 0xbc9f1d34) != 0x5b663814 {
    t.Fatalf("Hash error")
  }
  if Hash(data3, 0xbc9f1d34) != 0x323c078f {
    t.Fatalf("Hash error")
  }
  if Hash(data4, 0xbc9f1d34) != 0xed21633a {
    t.Fatalf("Hash error")
  }
  if Hash(data5, 0x12345678) != 0xf333dabb {
    t.Fatalf("Hash error")
  }
}
